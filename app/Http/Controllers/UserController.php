     * @param  \App\Models\User  $User
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
        if (!User::destroy($id)) {
            return redirect()->back();
        }
        Alert::success('Mantap', 'Data berhasil dihapus');
        return redirect()->route('user-management.index');

    }
